import java.util.Scanner;

/**
 * Minimum_Number_of_Ones
 */
 class Minimum_Number_of_Ones {
public static void main(String[] args) {
    
 
        Scanner sc = new Scanner(System.in);
        int inp, i, f, s;
        inp = sc.nextInt();
        for ( i = 0; i < inp; i++){
            f = sc.nextInt();


            s = f/2;
            System.out.println(s);
    }
    }
}